export function calculateDays(publishDate) {
  let currentDate = new Date();
  let publishDateObj = new Date(publishDate);
  let timeDifference = currentDate.getTime() - publishDateObj.getTime();
  let days = Math.ceil(timeDifference / (1000 * 3600 * 24));

  let dayDifference = "";
  if (days >= 365) {
    let x = days / 365;
    dayDifference = x + " years ago";
  } else if (days >= 30) {
    let x = days / 30;
    dayDifference = x + " months ago";
  } else if (days >= 7) {
    let x = days / 7;
    dayDifference = x + " weeks ago";
  } else {
    dayDifference = days + " days ago";
  }

  return dayDifference;
}
